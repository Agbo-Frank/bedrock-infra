#!/usr/bin/env bash
# terraform/apply.sh — terraform init + apply, save outputs, generate grading.json
# Called by main.sh. Do not run directly.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
source "$SCRIPT_DIR/../common.sh"

log_info "Initialising Terraform..."
terraform -chdir="$TERRAFORM_DIR" init -reconfigure

log_info "Applying Terraform (EKS takes ~15-20 minutes)..."
terraform -chdir="$TERRAFORM_DIR" apply -auto-approve


log_info "Saving outputs to $TF_OUTPUTS_FILE ..."

CLUSTER_NAME=$(get_tf_output cluster_name)
CLUSTER_ENDPOINT=$(get_tf_output cluster_endpoint)
REGION=$(get_tf_output region)
VPC_ID=$(get_tf_output vpc_id)
ASSETS_BUCKET=$(get_tf_output assets_bucket_name)
CART_IRSA_ARN=$(get_tf_output cart_irsa_role_arn)
GITHUB_ROLE_ARN=$(get_tf_output github_oidc_role_arn)
MYSQL_ENDPOINT=$(get_tf_output mysql_endpoint)
POSTGRES_ENDPOINT=$(get_tf_output postgres_endpoint)

cat > "$TF_OUTPUTS_FILE" <<EOF
# Auto-generated by terraform/apply.sh — do not commit this file
CLUSTER_NAME="$CLUSTER_NAME"
CLUSTER_ENDPOINT="$CLUSTER_ENDPOINT"
REGION="$REGION"
VPC_ID="$VPC_ID"
ASSETS_BUCKET="$ASSETS_BUCKET"
CART_IRSA_ARN="$CART_IRSA_ARN"
GITHUB_ROLE_ARN="$GITHUB_ROLE_ARN"
MYSQL_ENDPOINT="$MYSQL_ENDPOINT"
POSTGRES_ENDPOINT="$POSTGRES_ENDPOINT"
EOF

generate_helm_values


log_info "Generating grading.json (non-sensitive outputs only — safe to commit)..."
terraform -chdir="$TERRAFORM_DIR" output -json \
  | jq 'with_entries(select(.value.sensitive != true))' \
  > "$REPO_DIR/grading.json"
log_success "grading.json written — safe to commit and push"

log_info "Generating grading.credentials.json (sensitive — local/grader use only)..."
terraform -chdir="$TERRAFORM_DIR" output -json \
  | jq 'with_entries(select(.value.sensitive == true))' \
  > "$REPO_DIR/grading.credentials.json"
log_success "grading.credentials.json written — share with grader privately, never push"

log_success "Terraform apply complete."
echo ""
echo "  Cluster      : $CLUSTER_NAME"
echo "  Endpoint     : $CLUSTER_ENDPOINT"
echo "  Region       : $REGION"
echo "  VPC          : $VPC_ID"
echo "  Assets bucket: $ASSETS_BUCKET"
echo "  GitHub role  : $GITHUB_ROLE_ARN"
echo "  MySQL endpoint  : $MYSQL_ENDPOINT"
echo "  Postgres endpoint: $POSTGRES_ENDPOINT"
echo ""
log_warn "ACTION REQUIRED: Add the GitHub role ARN above as secret AWS_ROLE_ARN in your repo"
log_info "Next: ./scripts/k8s/main.sh all"
